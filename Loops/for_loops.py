# List of names
names = ["Emilio", "Carlos", "Andres", "Nelson", "German"]

# Prints each name
for name in names:
    print(name)

# String
hello = "hello"

# Iterate over the string
for char in hello:
    print(char * 5)

# iterate in a range. Last number of the range is not inclusive. Is just the number of times
# range(init, end, skip)
for numer in range(5):
    print(numer)

for number in range(0, 100, 5):
    print(number)

