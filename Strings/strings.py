# String concat
name = "Emilio"
lastname = "Jeldes"

print(name + " " + lastname)

# String format
print("{} {}".format(name, lastname))

# String interpolation
print(f"{name} {lastname}")

# lower() upper() string funcionts to change format
name.upper()
lastname.lower()

# .replace(char, new_character)
name.replace("o", "a")
print(name)
